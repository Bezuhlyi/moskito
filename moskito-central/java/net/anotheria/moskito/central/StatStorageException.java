package net.anotheria.moskito.central;

public class StatStorageException extends Exception {

	public StatStorageException() {
		super();
	}

	public StatStorageException(String message, Throwable cause) {
		super(message, cause);
	}

	public StatStorageException(String message) {
		super(message);
	}

	public StatStorageException(Throwable cause) {
		super(cause);
	}

}
